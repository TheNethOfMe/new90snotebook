const mongoose = require("mongoose");
const Schema = mongoose.Schema;

// TODO: Do we really need a 'name' field in these objects?

const UserSchema = new Schema({
  name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true
  },
  password: {
    type: String,
    required: true
  },
  date: {
    type: Date,
    default: Date.now
  }
});

module.exports = User = mongoose.model("users", UserSchema);
